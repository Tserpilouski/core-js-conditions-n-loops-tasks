/**
 * Sorts an array of numbers in ascending order in place.
 * Employ any sorting algorithm of your choice.
 * Take into account that the array can be very large. Consider how you can optimize your solution.
 * In this task, the use of methods of the Array and String classes is not allowed.
 *
 * @param {number[]} arr - The array to sort.
 * @return {number[]} The sorted array.
 *
 * @example:
 *  [2, 9, 5]       => [2, 5, 9]
 *  [2, 9, 5, 9]    => [2, 5, 9, 9]
 *  [-2, 9, 5, -3]  => [-3, -2, 5, 9]
 */
function partition(array, low, high) {
  const arr = array;
  const pivot = arr[high];
  let i = low - 1;

  for (let j = low; j < high; j += 1) {
    if (arr[j] < pivot) {
      i += 1;
      [arr[i], arr[j]] = [arr[j], arr[i]];
    }
  }

  [arr[i + 1], arr[high]] = [arr[high], arr[i + 1]];
  return i + 1;
}

function quickSort(arr, low, high) {
  if (low < high) {
    const pi = partition(arr, low, high);
    quickSort(arr, low, pi - 1);
    quickSort(arr, pi + 1, high);
  }
}

function sortByAsc(arr) {
  quickSort(arr, 0, arr.length - 1);
  return arr;
}

/**
 * Shuffles characters in a string so that the characters with an odd index are moved to the end of the string at each iteration.
 * Take into account that the string can be very long and the number of iterations is large. Consider how you can optimize your solution.
 * Usage of Array class methods is not allowed in this task.
 *
 * @param {string} str - The string to shuffle.
 * @param {number} iterations - The number of iterations to perform the shuffle.
 * @return {string} The shuffled string.
 *
 * @example:
 *  '012345', 1 => '024135'
 *  'qwerty', 1 => 'qetwry'
 *  '012345', 2 => '024135' => '043215'
 *  'qwerty', 2 => 'qetwry' => 'qtrewy'
 *  '012345', 3 => '024135' => '043215' => '031425'
 *  'qwerty', 3 => 'qetwry' => 'qtrewy' => 'qrwtey'
 */

function shuffleChar(str, iterations) {
  const n = str.length;
  if (n <= 1 || iterations === 0) return str;

  const effectiveIterations = n === 1 ? 1 : Math.ceil(Math.log2(n));
  const normalizedIterations = iterations % effectiveIterations;

  const result = Array(n);
  let indices = Array.from({ length: n }, (_, i) => i);

  for (let iter = 0; iter < normalizedIterations; iter += 1) {
    const tempIndices = Array(n);
    let oddIndex = 0;
    let evenIndex = 0;

    for (let i = 0; i < n; i += 1) {
      if (i % 2 === 0) {
        tempIndices[evenIndex] = indices[i];
        evenIndex += 1;
      } else {
        tempIndices[oddIndex] = indices[i];
        oddIndex += 1;
      }
    }
    indices = tempIndices;
  }

  for (let i = 0; i < n; i += 1) {
    result[indices[i]] = str[i];
  }

  return result.join('');
}

/**
 * Returns the nearest largest integer consisting of the digits of the given positive integer.
 * If there is no such number, it returns the original number.
 * Usage of String class methods is not allowed in this task.
 *
 * @example:
 * 12345    => 12354
 * 123450   => 123504
 * 12344    => 12434
 * 123440   => 124034
 * 1203450  => 1203504
 * 90822    => 92028
 * 321321   => 322113
 *
 * @param {number} number The source number
 * @returns {number} The nearest larger number, or original number if none exists.
 */
function getNearestBigger(number) {
  const digits = [];
  let temp = number;

  while (temp > 0) {
    digits.unshift(temp % 10);
    temp = Math.floor(temp / 10);
  }

  let i;
  for (i = digits.length - 2; i >= 0; i -= 1) {
    if (digits[i] < digits[i + 1]) {
      break;
    }
  }

  if (i < 0) return number;

  let j;

  for (j = digits.length - 1; j > i; j -= 1) {
    if (digits[j] > digits[i]) {
      break;
    }
  }

  const tempDigit = digits[i];
  digits[i] = digits[j];
  digits[j] = tempDigit;

  for (let k = i + 1; k < digits.length; k += 1) {
    for (let l = k + 1; l < digits.length; l += 1) {
      if (digits[k] > digits[l]) {
        const swap = digits[k];
        digits[k] = digits[l];
        digits[l] = swap;
      }
    }
  }

  let result = 0;
  for (let k = 0; k < digits.length; k += 1) {
    result = result * 10 + digits[k];
  }

  return result;
}

